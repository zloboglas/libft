#include "libft.h"

size_t			ft_strcfind(const char *s, char c)
{
	size_t		ln;

	ln = 0;
	while (s[ln] != c && s[ln] != '\0')
	{
		ln++;
	}
	if (s[ln] == '\0')
		return (-1);
	else
		return (ln);
}
